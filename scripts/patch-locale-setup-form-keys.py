def patch_locale_file(path: Path) -> bool:
    code = path.stem
    data = json.loads(path.read_text(encoding="utf-8"))
    setup = data.setdefault("setup", {})
    changed = False
    for key, translations in FORM_KEYS.items():
        val = translations.get(code) or translations["en"]
        if setup.get(key) != val:
            setup[key] = val
            changed = True
    meta = data.setdefault("_meta", {})
    if meta.get("setupFormKeys") != MARKER:
        meta["setupFormKeys"] = MARKER
        changed = True
    if changed:
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return changed


def main() -> int:
    if not LOCALES_DIR.is_dir():
        print(f"Skip — missing {LOCALES_DIR}", file=sys.stderr)
        return 0
    count = sum(1 for p in sorted(LOCALES_DIR.glob("*.json")) if patch_locale_file(p))
    print(f"Locale setup form keys: {count} file(s) updated")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
